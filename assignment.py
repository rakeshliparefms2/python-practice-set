s = '190'
print(int(s))  # str to int
print(float(s))  # to float
print(complex(s))  # to complex
print(bool('190'))  # to boolean
print(list(s))  # to list
print(tuple(s))  # to tuple
print(set(s))
##print(bytearray(s))
print(bin(int(s)))  # to binary
print(bytes(int(s)))   # to bytes
print(bytearray(int(s)))  # to bytearray
print(oct(int(s)))  # to octal
print(hex(int(s)))  # to hexadecimal
